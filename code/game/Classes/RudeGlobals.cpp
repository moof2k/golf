void RudeGlobals::GetPath(char *path)
{
#ifdef RUDE_SYMBIAN

	TFileName aPath=
		BaflUtils::DriveAndPathFromFullName(
			CEikonEnv::Static()->EikAppUi()->Application()->AppFullName());

	char *p = (char *) malloc(300 * sizeof(char));

	TPtr8 tptr((TUint8 *) p, 300);
	CnvUtfConverter::ConvertFromUnicodeToUtf8(tptr,aPath);

	for(int i = 0; i < tptr.Length(); i++)
		path[i] = p[i];
	path[i] = '\0';

	free(p);
#endif
	
#ifdef RUDE_IPHONE
	CFBundleRef bundle = CFBundleGetMainBundle();
	CFURLRef url = CFBundleCopyResourcesDirectoryURL(bundle);
	CFStringRef cfpath = CFURLCopyFileSystemPath(url, kCFURLPOSIXPathStyle);
	
	CFStringGetCString(cfpath, path, 512, kCFStringEncodingASCII);
#endif

}

